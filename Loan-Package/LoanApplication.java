    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        int term;
        
        if (primeRateTxt.getText().isEmpty() && loanNumberTxt.getText().isEmpty() && lastNameTxt.getText().isEmpty() &&
            loanAmtTxt.getText().isEmpty() && loanTypeOption.getSelectedIndex() == 0 && termTypeOption.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please fill out all required fields.");
            return;
        }
             
        try{
            int primeInterestRate = Integer.parseInt(primeRateTxt.getText());
            int loanNum = Integer.parseInt(loanNumberTxt.getText());
            String lastName = lastNameTxt.getText();
            float loanAmt = Float.parseFloat(loanAmtTxt.getText());
            String loanType = (String) loanTypeOption.getSelectedItem();
            String termType = (String) termTypeOption.getSelectedItem();
            
            
            if(primeRateTxt.getText().isEmpty() || loanNumberTxt.getText().isEmpty() || lastNameTxt.getText().isEmpty() || loanAmtTxt.getText().isEmpty() ||
            loanType.isEmpty() || termType.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please fill out all required fields.");
                return;
            }
            
            if(lastName.matches(".*\\d.*")){
                JOptionPane.showMessageDialog(this, "Invalid input.\n");
                return;
            }
            
            if(loanAmt > MAX_LOAN_AMOUNT){
                JOptionPane.showMessageDialog(this, "Loan amount cannot exceed $100,000\n");
                return;
            }
            
            boolean checkLoan = false;
            for(int i = 0; i < loans.size(); i++){
                if(loans.get(i).getLoanNum() == loanNum){ 
                    checkLoan = true;
                    break;
                }
            }
            
        if(checkLoan){
            clearBtnActionPerformed(evt);
            JOptionPane.showMessageDialog(this, "Loan number already exist.\n");
        }else{
                switch (termType) {
                    case "Short Term":
                        term = SHORT_TERM;
                        break;
                    case "Medium Term":
                        term = MEDIUM_TERM;
                        break;
                    case "Long Term":
                        term = LONG_TERM;
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Please select a term type.\n");
                        return;
                }
            
            switch(loanType){
                case "Business Loan":
                    loan = new BusinessLoan(loanNum, lastName, loanAmt, term, primeInterestRate);
                    interestTxt.setText("1");
                    loans.add(loan);
                    break;
                case "Personal Loan":
                    loan = new PersonalLoan(loanNum, lastName, loanAmt, term, primeInterestRate);
                    loan.setInterestRate(primeInterestRate);
                    interestTxt.setText("2");
                    loans.add(loan);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Please select a loan type.");
                    return; 
                    
            }
            JOptionPane.showMessageDialog(this, "Information Saved.\n");
            clearBtnActionPerformed(evt);
            primeRateTxt.setEditable(false);
            interestTxt.setEditable(false);
            }
        }catch(InputMismatchException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Invalid input.\n");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

