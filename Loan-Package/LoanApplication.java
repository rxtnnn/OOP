    private void viewLoansOptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewLoansOptionActionPerformed
        StringBuilder loanField = new StringBuilder();
        String select = (String) viewLoansOption.getSelectedItem();
        
        if(loans.isEmpty()){
           JOptionPane.showMessageDialog(this, "No loans to display. Please add loans first.\n");
       }else{
        switch(select){
            case "All Loan":   
                for(int i = 0; i < loans.size(); i++){
                    if(loans.get(i) instanceof BusinessLoan){
                        loanField.append("Business Loan\n").append(loans.get(i).toString()).append("\n\n");
                    }else if(loans.get(i) instanceof PersonalLoan){
                        loanField.append("Personal Loan\n").append(loans.get(i).toString()).append("\n\n");
                    }
                }
                break;
            case "Business Loan":
                for (int i = 0; i < loans.size(); i++) {
                    if (loans.get(i) instanceof BusinessLoan) {
                        loanField.append(loans.get(i).toString()).append("\n");
                    }
                }
            break;
            case "Personal Loan":
                for (int i = 0; i < loans.size(); i++) {
                    if (loans.get(i) instanceof PersonalLoan) {
                        loanField.append(loans.get(i).toString()).append("\n");
                    }
                }
            break;
            }
        }
        displayField.setText(loanField.toString());
    }//GEN-LAST:event_viewLoansOptionActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        StringBuilder searchResult = new StringBuilder();
       
        if(searchLoanNumTxt.getText().isEmpty() && loans.isEmpty()){
            JOptionPane.showMessageDialog(this, "No loans to display. Please add loans first.");
            return;
        }else if(searchLoanNumTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter a loan number.");
            return;
        }else if(loans.isEmpty()){
            JOptionPane.showMessageDialog(this, "No loans to display. Please add loans first.");
            return;
        }
       
        
        try{
            int searchLoanNum = Integer.parseInt(searchLoanNumTxt.getText());
            
            boolean found = false;
                for(int i = 0; i < loans.size(); i++){
                    if(loans.get(i).getLoanNum() == searchLoanNum) {
                        if(loans.get(i) instanceof BusinessLoan){
                            searchResult.append("Business Loan\n").append(loans.get(i).toString()).append("\n\n");
                        }else if(loans.get(i) instanceof PersonalLoan){
                            searchResult.append("Personal Loan\n").append(loans.get(i).toString()).append("\n\n");
                        }
                        found = true;
                        break;
                    }
                }
                
                if(!found){
                    JOptionPane.showMessageDialog(this, "Loan not found.\n");
                    return;
                }
            displayField.setText(searchResult.toString());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Please enter a valid loan number\n");
        }
        
    }//GEN-LAST:event_searchBtnActionPerformed

